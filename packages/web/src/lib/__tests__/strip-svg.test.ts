describe('stripSvg – preserves visual elements', () => {
  it('preserves <svg> root element with viewBox and dimensions', () => {
    const svg = `<!-- Author info -->
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 200 200" width="200" height="200">
  <rect x="10" y="10" width="180" height="180" fill="blue"/>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).toContain('viewBox="0 0 200 200"');
    expect(result).toContain('width="200"');
    expect(result).toContain('height="200"');
    expect(result).toContain('xmlns="http://www.w3.org/2000/svg"');
  });

  it('preserves <defs>, <style>, gradients, and filters', () => {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg">
  <!-- strip me -->
  <defs>
    <linearGradient id="grad1">
      <stop offset="0%" stop-color="red"/>
      <stop offset="100%" stop-color="blue"/>
    </linearGradient>
    <filter id="blur">
      <feGaussianBlur stdDeviation="3"/>
    </filter>
    <style>.icon { fill: #333; }</style>
  </defs>
  <rect width="100" height="100" fill="url(#grad1)"/>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).toContain('<defs>');
    expect(result).toContain('<linearGradient');
    expect(result).toContain('<filter');
    expect(result).toContain('<style>');
    expect(result).toContain('.icon { fill: #333; }');
    expect(result).toContain('url(#grad1)');
    expect(result).not.toContain('<!--');
  });

  it('preserves <animate> and <animateTransform> elements', () => {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg">
  <!-- animation -->
  <circle cx="50" cy="50" r="40">
    <animate attributeName="r" from="40" to="10" dur="1s" repeatCount="indefinite"/>
    <animateTransform attributeName="transform" type="rotate" from="0" to="360" dur="2s" repeatCount="indefinite"/>
  </circle>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).toContain('<animate');
    expect(result).toContain('<animateTransform');
    expect(result).not.toContain('<!--');
  });

  it('preserves <use>, <symbol>, <image> elements', () => {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg">
  <defs>
    <symbol id="icon"><circle cx="10" cy="10" r="10"/></symbol>
  </defs>
  <use href="#icon" x="0" y="0"/>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).toContain('<symbol');
    expect(result).toContain('<use');
    expect(result).toContain('href="#icon"');
  });

  it('preserves CSS class attributes and ids', () => {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg">
  <g class="layer" id="main-layer">
    <path class="shape" d="M10 10 L90 10" id="line1"/>
  </g>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).toContain('class="layer"');
    expect(result).toContain('id="main-layer"');
    expect(result).toContain('class="shape"');
    expect(result).toContain('id="line1"');
  });
});

