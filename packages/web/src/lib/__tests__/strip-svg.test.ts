    const result = fromBuffer(stripSvg(toBuffer(svg)));

    // Metadata stripped
    expect(result).not.toContain('<metadata');
    expect(result).not.toContain('<title>');
    expect(result).not.toContain('<desc>');
    expect(result).not.toContain('<!--');
    expect(result).not.toContain('<?inkscape');
    expect(result).not.toContain('xmlns:inkscape');
    expect(result).not.toContain('xmlns:sodipodi');
    expect(result).not.toContain('xmlns:dc');
