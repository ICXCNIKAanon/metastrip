/** Encodes a string as an ArrayBuffer (UTF-8). */
function toBuffer(s: string): ArrayBuffer {
  return new TextEncoder().encode(s).buffer;
}

/** Decodes an ArrayBuffer to a string (UTF-8). */
function fromBuffer(buf: ArrayBuffer): string {
  return new TextDecoder().decode(buf);
}

// ---------------------------------------------------------------------------
// isSvg
// ---------------------------------------------------------------------------

describe('isSvg', () => {
  it('returns true for a buffer starting with <svg', () => {
    expect(isSvg(toBuffer('<svg xmlns="http://www.w3.org/2000/svg"></svg>'))).toBe(true);
  });

  it('returns true for a buffer starting with <?xml', () => {
    const svg = '<?xml version="1.0" encoding="UTF-8"?>\n<svg xmlns="http://www.w3.org/2000/svg"></svg>';
    expect(isSvg(toBuffer(svg))).toBe(true);
  });

  it('returns true for SVG with leading whitespace', () => {
    expect(isSvg(toBuffer('  \n  <svg xmlns="http://www.w3.org/2000/svg"></svg>'))).toBe(true);
  });

  it('returns false for a JPEG buffer', () => {
    const jpeg = new Uint8Array([0xff, 0xd8, 0xff, 0xe0, 0x00, 0x10]);
    expect(isSvg(jpeg.buffer)).toBe(false);
  });

  it('returns false for a PNG buffer', () => {
    const png = new Uint8Array([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]);
    expect(isSvg(png.buffer)).toBe(false);
  });

  it('returns false for an empty buffer', () => {
    expect(isSvg(new ArrayBuffer(0))).toBe(false);
  });

  it('returns false for plain text that is not SVG', () => {
    expect(isSvg(toBuffer('Hello, world!'))).toBe(false);
  });

  it('returns false for HTML that starts with <!DOCTYPE>', () => {
    expect(isSvg(toBuffer('<!DOCTYPE html><html></html>'))).toBe(false);
  });
});

