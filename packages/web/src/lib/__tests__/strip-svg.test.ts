// stripSvg — metadata element removal
// ---------------------------------------------------------------------------

describe('stripSvg – removes <metadata> elements', () => {
  it('removes a <metadata> block', () => {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg">
  <metadata>
    <rdf:RDF>
      <cc:Work rdf:about="">
        <dc:creator>Jane Doe</dc:creator>
      </cc:Work>
    </rdf:RDF>
  </metadata>
  <rect width="100" height="100"/>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).not.toContain('<metadata>');
    expect(result).not.toContain('dc:creator');
    expect(result).not.toContain('Jane Doe');
    expect(result).toContain('<rect');
  });

  it('removes multiline metadata block', () => {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg">
  <metadata id="meta">
    <rdf:RDF xmlns:rdf="http://www.w3.org/1999/02/22-rdf-syntax-ns#">
      <cc:Work><dc:title>My Drawing</dc:title></cc:Work>
    </rdf:RDF>
  </metadata>
  <circle cx="50" cy="50" r="40"/>
</svg>`;
    const result = fromBuffer(stripSvg(toBuffer(svg)));
    expect(result).not.toContain('<metadata');
    expect(result).not.toContain('My Drawing');
    expect(result).toContain('<circle');
  });
});

