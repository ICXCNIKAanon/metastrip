    expect(result).not.toContain('inkscape:label');
    expect(result).not.toContain('inkscape:groupmode');
    expect(result).not.toContain('sodipodi:nodetypes');
    expect(result).not.toContain('Jane Doe');
    expect(result).not.toContain('Company Logo');
